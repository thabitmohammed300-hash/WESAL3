// Current language
let currentLang = 'ar';

// Language switching function
function switchLanguage(lang) {
  currentLang = lang;
  
  // Update HTML lang and dir attributes
  document.documentElement.lang = lang;
  document.documentElement.dir = (lang === 'ar') ? 'rtl' : 'ltr';
  
  // Update title
  const titles = {
    ar: 'وصال | منصة دردشة خاصة',
    en: 'Wesal | Private Chat Platform',
    de: 'Wesal | Private Chat-Plattform'
  };
  const chatTitles = {
    ar: 'وصال - المحادثة',
    en: 'Wesal - Chat',
    de: 'Wesal - Chat'
  };
  const currentTitle = document.title.includes('المحادثة') || document.title.includes('Chat') ? chatTitles[lang] : titles[lang];
  document.title = currentTitle;
  
  // Update all elements with data-i18n
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (translations[lang][key]) {
      // If element has children, only update text nodes, not child elements
      if (element.children.length > 0) {
        // Find the first text node and update it
        for (let node of element.childNodes) {
          if (node.nodeType === Node.TEXT_NODE) {
            node.textContent = translations[lang][key];
            break;
          }
        }
      } else {
        element.textContent = translations[lang][key];
      }
    }
  });
  
  // Update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
    const key = element.getAttribute('data-i18n-placeholder');
    if (translations[lang][key]) {
      element.placeholder = translations[lang][key];
    }
  });
  
  // Update active language button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.remove('active');
    if (btn.getAttribute('data-lang') === lang) {
      btn.classList.add('active');
    }
  });
}

// Initialize language switching on page load
document.addEventListener('DOMContentLoaded', function() {
  // Add event listeners to language buttons
  document.querySelectorAll('.lang-btn').forEach(button => {
    button.addEventListener('click', function() {
      const lang = this.getAttribute('data-lang');
      switchLanguage(lang);
    });
  });
  
  // Set initial language
  switchLanguage('ar');
});

const socket = io();

let currentRoom = null;

function showLanding() {
  document.getElementById('landing').style.display = 'block';
  document.getElementById('create-room').style.display = 'none';
  document.getElementById('join-room').style.display = 'none';
  document.getElementById('chat').style.display = 'none';
}

function showCreate() {
  document.getElementById('landing').style.display = 'none';
  document.getElementById('create-room').style.display = 'block';
  document.getElementById('join-room').style.display = 'none';
  document.getElementById('chat').style.display = 'none';
}

function showJoin() {
  document.getElementById('landing').style.display = 'none';
  document.getElementById('create-room').style.display = 'none';
  document.getElementById('join-room').style.display = 'block';
  document.getElementById('chat').style.display = 'none';
}

function createRoom() {
  const roomId = document.getElementById('create-room-id').value.trim();
  const password = document.getElementById('create-room-password').value;
  
  if (!roomId || !password) {
    alert(translations[currentLang]['room-id-placeholder']);
    return;
  }
  
  socket.emit('create-room', { roomId, password });
}

function joinRoom() {
  const roomId = document.getElementById('room-id').value.trim();
  const password = document.getElementById('room-password').value;
  
  if (!roomId || !password) {
    alert(translations[currentLang]['invalid-password']);
    return;
  }
  
  socket.emit('join-room', { roomId, password });
}

function leaveRoom() {
  if (currentRoom) {
    socket.emit('leave-room', currentRoom);
    currentRoom = null;
  }
  document.getElementById('chat').style.display = 'none';
  document.getElementById('landing').style.display = 'block';
}

function sendMessage() {
  const input = document.getElementById('message-input');
  const message = input.value.trim();
  if (message && currentRoom) {
    socket.emit('send-message', { roomId: currentRoom, message });
    input.value = '';
    displayMessage(message, 'right');
  }
}

function handleKeyPress(event) {
  if (event.key === 'Enter') {
    sendMessage();
  }
}

function handleFileSelect(event) {
  const file = event.target.files[0];
  if (file) {
    sendFile(file);
  }
}

function sendFile(file) {
  if (!currentRoom) return;

  const isImage = file.type.startsWith('image/');
  const isVideo = file.type.startsWith('video/');
  if (!isImage && !isVideo) {
    alert('يمكنك إرسال صور أو مقاطع فيديو فقط.');
    return;
  }

  const reader = new FileReader();
  reader.onload = () => {
    const fileData = {
      name: file.name,
      type: file.type,
      dataUrl: reader.result,
    };

    socket.emit('send-message', { roomId: currentRoom, file: fileData });
    displayMedia(fileData, 'right');
    document.getElementById('file-input').value = '';
  };
  reader.readAsDataURL(file);
}

function displayMessage(message, side) {
  const messages = document.getElementById('messages');
  const msgDiv = document.createElement('div');
  msgDiv.className = `bubble bubble-${side}`;
  msgDiv.textContent = message;
  messages.appendChild(msgDiv);
  messages.scrollTop = messages.scrollHeight;
}

function displayMedia(fileData, side) {
  const messages = document.getElementById('messages');
  const msgDiv = document.createElement('div');
  msgDiv.className = `bubble bubble-${side} message-bubble-file`;

  if (fileData.type.startsWith('image/')) {
    const img = document.createElement('img');
    img.src = fileData.dataUrl;
    img.alt = fileData.name;
    msgDiv.appendChild(img);
  } else if (fileData.type.startsWith('video/')) {
    const video = document.createElement('video');
    video.src = fileData.dataUrl;
    video.controls = true;
    msgDiv.appendChild(video);
  }

  const nameLabel = document.createElement('div');
  nameLabel.className = 'file-name';
  nameLabel.textContent = fileData.name;
  msgDiv.appendChild(nameLabel);

  messages.appendChild(msgDiv);
  messages.scrollTop = messages.scrollHeight;
}

socket.on('room-joined', (roomId) => {
  currentRoom = roomId;
  const roomElement = document.getElementById('current-room');
  if (roomElement) {
    roomElement.textContent = roomId;
  }
  document.getElementById('create-room').style.display = 'none';
  document.getElementById('join-room').style.display = 'none';
  document.getElementById('chat').style.display = 'block';
});

socket.on('room-full', () => {
  // Clear inputs
  document.getElementById('create-room-id').value = '';
  document.getElementById('create-room-password').value = '';
  document.getElementById('room-id').value = '';
  document.getElementById('room-password').value = '';
});

socket.on('room-error', (data) => {
  let message = translations[currentLang]['room-not-found'];
  if (data.message === 'Invalid password') {
    message = translations[currentLang]['invalid-password'];
  } else if (data.message === 'Room already exists') {
    message = translations[currentLang]['room-already-exists'];
  }
  alert(message);
});

socket.on('room-full-error', () => {
  alert(translations[currentLang]['room-full-error']);
});

socket.on('receive-message', (data) => {
  if (data.file) {
    displayMedia(data.file, 'left');
  } else {
    displayMessage(data.message, 'left');
  }
});